echo -e ':::   ::   ::::::   :::::::   :::::::     :::   ::::::::     :::'
echo -e '\e[0m'

# Variables
EXECUTE=nibid
CHAIN_ID=nibiru-itn-1
REPO=https://github.com/NibiruChain/nibiru.git
PROJECT_FOLDER=nibiru
SYSTEM_FOLDER=.nibid
VERSION=v0.19.2
GENESIS_FILE=https://snapshots.kjnodes.com/nibiru-testnet/genesis.json
ADDRBOOK=https://snapshots.kjnodes.com/nibiru-testnet/addrbook.json
PORT=26
DENOM=unibi
GO_VERSION=$(curl -L https://golang.org/VERSION?m=text | sed 's/^go//') 
PEERS="e2ad22b7cefbddd747c29d90882561e566ff2d3e@65.109.50.106:26656,74405e27923c1efe97fc678aa9f0357537a9b311@161.97.64.38:26656,b3a2a298c6a84c503253d120e3eee0c54cea90fd@137.184.193.235:20356,b9f203a7d45a2a2766ff144ea9cc680987886772@85.239.242.186:26656,bd4e84bd7b14201661c958c6cb6a1de2a27078ed@95.217.156.62:26656,0faa013496da308cf091099bb736f512f17ab380@185.144.99.55:26656,a03eaa525bd984d713fd9b000a89163dc7516a83@185.207.250.222:26656,5160aa16c18a37d25c3e667c80de83279715f2aa@195.2.75.252:26656,d5519e378247dfb61dfe90652d1fe3e2b3005a5b@65.109.68.190:13956,ce2dcfe5794bed1d4b32b8a43b32afc5d5e435f2@135.181.114.98:46656,f98a8229e5dc6da6d5e49fd4e115472df3d1773c@95.9.36.100:26656,b253cc6155ec59ea623f3f453d2f5a4b9c6d08fc@212.15.59.91:39656"
SEEDS="3f472746f46493309650e5a033076689996c8881@nibiru-testnet.rpc.kjnodes.com:13959"

sleep 2

echo "export EXECUTE=${EXECUTE}" >> $HOME/.bash_profile
echo "export CHAIN_ID=${CHAIN_ID}" >> $HOME/.bash_profile
echo "export SYSTEM_FOLDER=${SYSTEM_FOLDER}" >> $HOME/.bash_profile
echo "export PROJECT_FOLDER=${PROJECT_FOLDER}" >> $HOME/.bash_profile
echo "export VERSION=${VERSION}" >> $HOME/.bash_profile
echo "export REPO=${REPO}" >> $HOME/.bash_profile
echo "export GENESIS_FILE=${GENESIS_FILE}" >> $HOME/.bash_profile
echo "export ADDRBOOK=${ADDRBOOK}" >> $HOME/.bash_profile
